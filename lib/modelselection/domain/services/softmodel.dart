import 'package:csv/csv.dart';

class SoftwareDevelopmentModel {
  final String model;
  final String projectSize;
  final String projectType;
  final String methodology;
  final String flexibility;
  final String communicationCollaboration;
  final String planningDocumentation;
  final String riskManagement;
  final String qualityAssurance;
  final String releaseStrategy;
  final String customerInvolvement;
  final String resourceManagement;
  final String metricsMeasurement;
  final int minTeamSize;
  final int maxTeamSize;
  final String preferredUseCases;

  SoftwareDevelopmentModel({
