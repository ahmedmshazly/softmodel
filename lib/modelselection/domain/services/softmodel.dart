    required this.projectSize,
    required this.projectType,
    required this.methodology,
    required this.flexibility,
    required this.communicationCollaboration,
    required this.planningDocumentation,
    required this.riskManagement,
    required this.qualityAssurance,
    required this.releaseStrategy,
    required this.customerInvolvement,
    required this.resourceManagement,
    required this.metricsMeasurement,
    required this.minTeamSize,
    required this.maxTeamSize,
    required this.preferredUseCases,
  });
  
}


